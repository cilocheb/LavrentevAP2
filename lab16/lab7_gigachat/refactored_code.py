# Функция сложения двух чисел с типизацией аргументов и результата
def sum_numbers(x: int, y: int) -> int:
    """Возвращает сумму двух целых чисел"""
    return x + y

# Функция вычисления среднего арифметического с проверкой типов
def average(a: float, b: float, c: float) -> float:
    """
    Вычисляет среднее арифметическое числа a, b и c 
    (результат делится пополам после вычисления суммы).
    
    Аргументы:
        a (float): первое число
        b (float): второе число
        c (float): третье число
        
    Возвращает:
        float: результат деления полусуммы трех чисел на два
    """
    try:
        # Проверка корректности входных значений
        assert isinstance(a, (int, float)), 'Первый аргумент должен быть числом'
        assert isinstance(b, (int, float)), 'Второй аргумент должен быть числом'
        assert isinstance(c, (int, float)), 'Третий аргумент должен быть числом'
        return (a * b + c) / 2
    except AssertionError as e:
        raise ValueError(f'Ошибка ввода: {e}')

# Функция обработки списка целых чисел
def double_even_triple_odd(lst: list[int]) -> list[int]:
    """
    Обрабатывает список целых чисел, удваивая четные элементы и утраивая нечетные.
    
    Аргумент:
        lst (list[int]): список целых чисел
        
    Возвращает:
        list[int]: новый список, где четные числа удвоены, а нечетные утроены
    """
    return [x * 2 if x % 2 == 0 else x * 3 for x in lst]

